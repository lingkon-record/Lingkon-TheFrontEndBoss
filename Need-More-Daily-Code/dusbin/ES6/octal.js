let a = 0o51;
console.log(a); // 41

// Binary literals
let e = parseInt('111',2);
console.log(e); // 7

let f = 0b111;
console.log(f); // 7


// Summary
// Octal literals start with 0o followed by a sequence of numbers between 0 and 7.
// Binary literals start with 0b followed by a sequence of number 0 and 1.